// 괘 ID로 괘 정보 찾기
export function getGuaById(id: number): GuaInfo | undefined {
  return guaData.find(gua => gua.id === id);
}

// 상괘, 하괘 이름으로 괘 찾기
export function getGuaByTrigrams(upper: string, lower: string): GuaInfo | undefined {
  return guaData.find(gua => gua.upperGua === upper && gua.lowerGua === lower);
}

// 효 배열로 괘 찾기 (아래에서 위로, 0=음, 1=양)
export function getGuaByYaos(yaos: number[]): GuaInfo | undefined {
  if (yaos.length !== 6) return undefined;
  
  // 하괘 (초효, 이효, 삼효)
  const lowerBinary = `${yaos[0]}${yaos[1]}${yaos[2]}`;
  // 상괘 (사효, 오효, 상효)
  const upperBinary = `${yaos[3]}${yaos[4]}${yaos[5]}`;
  
  const lowerGua = guaBinary[lowerBinary as keyof typeof guaBinary];
  const upperGua = guaBinary[upperBinary as keyof typeof guaBinary];
  
  if (!lowerGua || !upperGua) return undefined;
  
  const guaNames: Record<string, string> = {
    'qian': '건',
    'kun': '곤',
    'zhen': '진',
    'xun': '손',
    'kan': '감',
    'li': '리',
    'gen': '간',
    'dui': '태',
  };
  
  return getGuaByTrigrams(guaNames[upperGua], guaNames[lowerGua]);
}

// 효 값에서 음양 판단 (7, 9 = 양 / 6, 8 = 음)
export function isYang(value: YaoType): boolean {
  return value === 7 || value === 9;
}

// 노효 여부 판단 (6, 9는 변하는 효)
export function isChanging(value: YaoType): boolean {
  return value === 6 || value === 9;
}

// 변괘 계산
export function getChangedYaos(yaos: YaoType[]): number[] {
  return yaos.map(yao => {
    if (yao === 9) return 0; // 노양 -> 음으로 변화
    if (yao === 6) return 1; // 노음 -> 양으로 변화
    return isYang(yao) ? 1 : 0;
  });
}
